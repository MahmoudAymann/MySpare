public class Notiu {
    String ahmed;
    String date;

    public Notiu(String ahmed, String date) {
        this.ahmed = ahmed;
        this.date = date;
    }

    public String getAhmed() {
        return ahmed;
    }

    public void setAhmed(String ahmed) {
        this.ahmed = ahmed;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }
}
